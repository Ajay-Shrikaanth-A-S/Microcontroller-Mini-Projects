
; 7-Segment Display program for 8051
ORG 0000H

START:
    MOV DPTR, #LOOKUP_TABLE
    MOV R0, #00H

DISPLAY_LOOP:
    MOV A, R0
    MOVC A, @A+DPTR
    MOV P1, A
    ACALL DELAY
    INC R0
    CJNE R0, #0AH, DISPLAY_LOOP
    MOV R0, #00H
    SJMP DISPLAY_LOOP

LOOKUP_TABLE:
    DB 3FH, 06H, 5BH, 4FH, 66H, 6DH, 7DH, 07H, 7FH, 6FH

DELAY:
    MOV R1, #255
    MOV R2, #255

DELAY_LOOP1:
    MOV R2, #255

DELAY_LOOP2:
    DJNZ R2, DELAY_LOOP2
    DJNZ R1, DELAY_LOOP1

    RET

END
